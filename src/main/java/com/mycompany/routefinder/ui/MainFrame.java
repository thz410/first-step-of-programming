    private void loadLocations(){
        startCombo.removeAllItems();
        endCombo.removeAllItems();
        
        //Get real data from the City
        List<Location> cities = CityMap.getInstance().getAllLocations();
        
        for(Location city : cities){
            startCombo.addItem(city);
            endCombo.addItem(city);
        }
    }
    
    private void refreshHistory(){
        historyModel.clear();
        
        //Get real data from CityMap
        List<String> logs = CityMap.getInstance().getSearchHistory();
        
        //Newest at top
        for(int i = logs.size()-1; i>=0; i--){
            historyModel.addElement(logs.get(i));
        }
    }

