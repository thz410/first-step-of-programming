    private void findButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findButtonActionPerformed
        // TODO add your handling code here:
        Location start = (Location) startCombo.getSelectedItem();
        Location end = (Location) endCombo.getSelectedItem();
        
        // Validation
        if(start.equals(end)){
            JOptionPane.showMessageDialog(this, "Start and Destination cannot be the same!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        //Calculation
        resultArea.setText("Calculating path from " + start.getName() + " to " + end.getName() + "...\n");
        
        PathResult result = CityMap.getInstance().findShortestPath(start, end);
        this.currentResult = result;
        
        //Display Result
        if(result == null){
            resultArea.append("\n❌ NO ROUTE FOUND.\n");
            resultArea.append("These locations are not connected.");
        }
        else{
            resultArea.append("\n✅ ROUTE FOUND!\n");
            resultArea.append("---------------------------------------\n");
            resultArea.append("Path: " + result.getFormattedPath() + "\n");
            resultArea.append("Total Distance: " + result.getTotalDistance() + "km\n");
        }
        
        if (graphPanel != null && mapDialog != null &&  mapDialog.isVisible()) {
            if(result != null) {
                graphPanel.setHighlightPath(result.getPath());
            } 
            else {
                graphPanel.setHighlightPath(null);
            }
        }
        
        CityMap.getInstance().recordSearch(start, end, result);
        
        refreshHistory();
    }//GEN-LAST:event_findButtonActionPerformed

    private void showMapButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showMapButtonActionPerformed
        // TODO add your handling code here:
        //JDialog for Map
        if(mapDialog == null){
            mapDialog = new javax.swing.JDialog(this, "Network Visualization", false);
            mapDialog.setSize(600, 500);
            mapDialog.setLocationRelativeTo(this);
            //Graph Panel
            graphPanel = new com.mycompany.routefinder.ui.GraphPanel();
            mapDialog.add(graphPanel);
        }
        
                
        // Show the path in the panel
        if(this.currentResult != null){
            graphPanel.setHighlightPath(this.currentResult.getPath());
        }
        else{
            graphPanel.setHighlightPath(null);
        }
        
        //mapDialog.add(graphPanel);
        if(!mapDialog.isVisible()){
            mapDialog.setVisible(true);
        }
        mapDialog.toFront();
    }//GEN-LAST:event_showMapButtonActionPerformed

